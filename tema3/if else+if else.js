//Concatenaciones if else + if else

const nota = 6;

if (nota===5) {
    console.log("Felicidades,aprobaste")
} else if(nota===4) {
    console.log("Bien pero podrias haberlo hecho mejor")
}else if(nota===3) {
    console.log("Aprobaste con lo justo")
}else if(nota===2) {
    console.log("No aprobaste por poco")
}else if(nota===1) {
    console.log("No aprobaste, estudia un poco mas")
}else{
    console.log("Error, introduce una nota entre el 1 y el 5")
}